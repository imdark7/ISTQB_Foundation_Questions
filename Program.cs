        private static void ParseQuestions()
        {
            var text = System.IO.File.OpenText(@"C:\Users\Dark\Documents\Visual Studio 2017\Projects\ISTQB_Foundation_Questions\ISTQB_Foundation_Questions\bin\Debug\questions.txt").ReadToEnd();
            var questions = text.Split(new[] { "<<<<<< =================== >>>>>>" }, StringSplitOptions.None);

            foreach (var question1 in questions)
            {
                var question = question1.Trim();
                var start = question.IndexOf(":", StringComparison.Ordinal) + 1;
                var length = question.IndexOf("A.", StringComparison.Ordinal) - start;
                var veryBegin = question.IndexOf("Q. ", StringComparison.Ordinal);
                var number = int.Parse(question.Substring(veryBegin + 3, start - 4));
                var questionText = question.Substring(start, length);
                var questionId = SqlHelper.InsertQuestion(new Question {Id = number, EnglishText = questionText.Trim() }).Value;

                

                start = question.IndexOf("A.", StringComparison.Ordinal) + 2;
                length = question.IndexOf("B.", StringComparison.Ordinal) - start;
                var answerText = question.Substring(start, length);
                SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "A"});

                start = question.IndexOf("B.", StringComparison.Ordinal) + 2;
                length = question.IndexOf("C.", StringComparison.Ordinal) - start;
                answerText = question.Substring(start, length);
                SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "B" });

                if (question.Contains("D."))
                {
                    start = question.IndexOf("C.", StringComparison.Ordinal) + 2;
                    length = question.IndexOf("D.", StringComparison.Ordinal) - start;
                    answerText = question.Substring(start, length);
                    SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "C" });

                    if (question.Contains("E."))
                    {
                        start = question.IndexOf("D.", StringComparison.Ordinal) + 2;
                        length = question.IndexOf("E.", StringComparison.Ordinal) - start;
                        answerText = question.Substring(start, length);
                        SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "D" });

                        start = question.IndexOf("E.", StringComparison.Ordinal) + 2;
                        answerText = question.Substring(start);
                        SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "E" });
                    }
                    else
                    {
                        start = question.IndexOf("D.", StringComparison.Ordinal) + 2;
                        answerText = question.Substring(start);
                        SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "D" });
                    }
                }
                else
                {
                    start = question.IndexOf("C.", StringComparison.Ordinal) + 2;
                    answerText = question.Substring(start);
                    SqlHelper.InsertAnswer(new Answer { EnglishText = answerText.Trim(), QuestionId = questionId, IsCorrect = CorrectAnswers[number] == "C" });
                }
                
            }
        }

